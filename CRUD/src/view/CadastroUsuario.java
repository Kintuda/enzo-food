    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        Usuarios usuario = new Usuarios();
        String nome = nomeTexto.getText();
        String cidade = cidadeTexto.getText();
        String telefone = telefoneTexto.getText();
        String endereco = enderecoTexto.getText();
        Date data = new Date();
        if (nome.isEmpty() || cidade.isEmpty() || endereco.isEmpty() || endereco.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Dados Inv√°lidos");
        } else {
            usuario.setNome(nome);
            usuario.setCidade(cidade);
            usuario.setEndereco(endereco);
            usuario.setTelefone(telefone);
            usuario.setData(data);
            CRUD.Cadastrar(usuario);
            JOptionPane.showMessageDialog(null, "Sucesso Dados Cadastrados");
        }

    }//GEN-LAST:event_button1ActionPerformed

